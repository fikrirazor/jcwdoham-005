// Diameter, Circumference, and Area of a Circle
// Diameter adalah garis lurus yang menghubungkan dua titik pada lingkaran dan melewati pusat lingkaran.
// Circumference adalah keliling lingkaran.
// Area adalah luas lingkaran.
let radius = 5;

// Rumus diameter: d = 2 * r
let diameter = 2 * radius;

// Rumus circumference: C = 2 * π * r
let circumference = 2 * 3.14 * radius;

// Rumus area: A = π * r^2
let area = 3.14 * radius ** 2;

console.log("Diameter dari lingkaran adalah : " + diameter);
console.log("Circumference dari lingkaran adalah : " + circumference);
console.log("Area dari lingkaran adalah : " + area);